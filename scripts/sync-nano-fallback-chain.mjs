import { createHash } from 'node:crypto';
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const checkMode = process.argv.includes('--check');
const scriptDir = dirname(fileURLToPath(import.meta.url));
const appRoot = resolve(scriptDir, '..');
const workspaceRoot = resolve(appRoot, '..');
const nanoRoot = resolve(workspaceRoot, 'nano-banana-pro');
const snapshotPath = resolve(appRoot, 'src/lib/nanoBananaFallbackSnapshot.js');

const sourcePaths = {
  packageJson: resolve(nanoRoot, 'package.json'),
  fallbackHistory: resolve(nanoRoot, 'src/lib/fallback-chain-history.js'),
  gemini: resolve(nanoRoot, 'src/lib/gemini.js'),
  openaiText: resolve(nanoRoot, 'src/lib/openai-text.js'),
};

try {
  main();
} catch (error) {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
}

function main() {
  assertSources();

  const snapshot = buildSnapshot();
  const output = formatSnapshot(snapshot);

  if (checkMode) {
    const current = existsSync(snapshotPath) ? readFileSync(snapshotPath, 'utf8') : '';
    if (normalizeNewlines(current) !== normalizeNewlines(output)) {
      console.error(
        'Nano Banana Pro fallback順列の同期差分があります。npm run sync:nano-fallback を実行してから再確認してください。',
      );
      process.exit(1);
    }
    console.log('Nano Banana Pro fallback順列の同期状態は最新です。');
    return;
  }

  writeFileSync(snapshotPath, output, 'utf8');
  console.log(`Nano Banana Pro fallback順列を同期しました: ${relative(appRoot, snapshotPath)}`);
}

function buildSnapshot() {
  const packageJson = JSON.parse(readFileSync(sourcePaths.packageJson, 'utf8'));
  const geminiText = extractModelIds(readFileSync(sourcePaths.gemini, 'utf8'), 'TEXT_MODEL_IDS');
  const openaiText = extractModelIds(readFileSync(sourcePaths.openaiText, 'utf8'), 'TEXT_MODEL_IDS');
  const syncedAt = checkMode ? readExistingSyncedAt() : new Date().toISOString();
  const sourceFiles = Object.values(sourcePaths).map(sourceInfo);

  return {
    sourceApp: 'nano-banana-pro',
    sourceVersion: String(packageJson.version),
    syncedAt,
    sourceFingerprint: hashText(sourceFiles.map((file) => `${file.path}:${file.sha256}`).join('\n')),
    sourceFiles,
    chains: {
      geminiText: {
        provider: 'gemini',
        sourceFile: 'nano-banana-pro/src/lib/gemini.js',
        models: toModelEntries(geminiText),
      },
      openaiText: {
        provider: 'openai',
        sourceFile: 'nano-banana-pro/src/lib/openai-text.js',
        models: toModelEntries(openaiText),
      },
    },
    updateWorkflow:
      '物語バズメーカーのバグフィックスやアップデート時は npm run check:nano-fallback を必ず実行し、差分があれば npm run sync:nano-fallback でこのスナップショットを更新する。',
  };
}

function extractModelIds(source, constName) {
  const match = source.match(new RegExp(`const\\s+${constName}\\s*=\\s*\\[([\\s\\S]*?)\\];`));
  if (!match) {
    throw new Error(`${constName} was not found in Nano Banana Pro source.`);
  }
  const ids = [...match[1].matchAll(/["']([^"']+)["']/g)].map((item) => item[1]);
  if (ids.length === 0) {
    throw new Error(`${constName} did not contain model ids.`);
  }
  return ids;
}

function toModelEntries(ids) {
  return ids.map((id, index) => ({
    id,
    role: index === 0 ? 'Primary' : index === ids.length - 1 ? 'Fallback' : 'Backup',
    order: index + 1,
  }));
}

function sourceInfo(filePath) {
  const content = readFileSync(filePath, 'utf8');
  return {
    path: relative(workspaceRoot, filePath).replaceAll('\\', '/'),
    bytes: Buffer.byteLength(content, 'utf8'),
    sha256: hashText(content),
  };
}

function hashText(value) {
  return createHash('sha256').update(value).digest('hex');
}

function readExistingSyncedAt() {
  if (!existsSync(snapshotPath)) return new Date().toISOString();
  const match = readFileSync(snapshotPath, 'utf8').match(/"syncedAt":\s*"([^"]+)"/);
  return match?.[1] ?? new Date().toISOString();
}

function formatSnapshot(snapshot) {
  return `// Generated by scripts/sync-nano-fallback-chain.mjs.\n// Run npm run sync:nano-fallback after Nano Banana Pro fallback-chain updates.\n\nexport const NANO_BANANA_FALLBACK_SNAPSHOT = ${JSON.stringify(snapshot, null, 2)};\n`;
}

function assertSources() {
  for (const filePath of Object.values(sourcePaths)) {
    if (!existsSync(filePath)) {
      throw new Error(`Nano Banana Pro source not found: ${filePath}`);
    }
  }
}

function normalizeNewlines(value) {
  return value.replace(/\r\n/g, '\n');
}
